'use strict'


function retrair() {
  var dashboard = document.querySelector('.dashboard')
  var estabe = document.querySelector('.estabe')
  var esta = document.querySelector('.esta')
  var estabele = document.querySelector('.estabele')
  var copy = document.querySelector('.copyright')

  dashboard.classList.toggle('hide')
  estabe.classList.toggle('item-hide')
  esta.classList.toggle('item-hide')
  estabele.classList.toggle('item-hide')
  copy.classList.toggle('item-hide')



}